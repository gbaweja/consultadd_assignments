# Nested if else statement
i = 10
if (i == 10): 
    #  First if statement 
    print ("\ni is 10")
    if (i < 15): 
        print ("\ni is smaller than 15") 
    # Nested - if statement 
    # Will only be executed if statement above 
    # it is true 
    if (i < 12): 
        print ("\ni is smaller than 12 too") 
    else: 
        print ("\ni is greater than 15") 




